package demo;

public class loopsprg {

	public static void main(String[] args) {
		int a=21;
		while(a>=20) {
			System.out.println("working!!");
			a--;
		}
		
		//do-while loop
		int b=50;
		do {
			System.out.println("working loop!!");
			a++;
		}
		while(b>=60);
		
		

	}

}
